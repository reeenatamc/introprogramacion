package Bimestre2.Semana1.Semana2;

public class ArreglosCuadrado {
    public static void main(String[] args) {
        int numeros[] = {2, 3, 5, 7, 11, 13};
        int c [] = new int [numeros.length];
        for (int indice = 0; indice < c.length ; indice++){
            c[indice]=numeros[indice]*numeros[indice];
        }
        for (int indice = 0; indice < c[indice]; indice++) {
            System.out.println(c[indice]);

        }
    }
}
